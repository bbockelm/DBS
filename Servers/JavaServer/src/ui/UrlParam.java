package ui;
public class UrlParam {
	private String lfn;
	private String qmd;
	private String blockName;
	private String tier;
	private String path;
	private String oldSe;
	private String newSe;
	private String lumi;
	private String run;

	public String getLfn() {
		return lfn;
	}
	public void setLfn(String lfn) {
		this.lfn = lfn;
	}

	public String getQmd() {
		return qmd;
	}
	public void setQmd(String qmd) {
		this.qmd = qmd;
	}

	public String getBlockName() {
		return blockName;
	}
	public void setBlockName(String blockName) {
		this.blockName = blockName;
	}

	public String getTier() {
		return tier;
	}
	public void setTier(String tier) {
		this.tier = tier;
	}

	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}

	public String getOldSe() {
		return oldSe;
	}
	public void setOldSe(String oldSe) {
		this.oldSe = oldSe;
	}

	public String getNewSe() {
		return newSe;
	}
	public void setNewSe(String newSe) {
		this.newSe = newSe;
	}

	public String getLumi() {
		return lumi;
	}
	public void setLumi(String lumi) {
		this.lumi = lumi;
	}

	public String getRun() {
		return run;
	}
	public void setRun(String run) {
		this.run = run;
	}



}
